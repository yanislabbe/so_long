#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	size_t	count;

	if (!dst && !src)
		return (NULL);
	if (dst > src)
	{
		while (len > 0)
		{
			((char *)dst)[len - 1] = ((char *)src)[len - 1];
			len--;
		}
	}	
	else
	{
		count = 0;
		while (len > count)
		{
			((char *)dst)[count] = ((char *)src)[count];
			count++;
		}
	}
	return (dst);
}
